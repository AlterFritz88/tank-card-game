import type { Nation } from "./types";

/**
 * Национальная особенность — пассивная способность, общая для всех штабов одной
 * нации (поверх их собственной {@link HeadquartersAbility}). Срабатывает, когда
 * армия выстраивает определённую боевую формацию на поле боя или в тылу.
 */
export type NationalAbilityId =
  | "cohesion" // СССР — «Сплочение»
  | "supply_line" // США — «Линия снабжения»
  | "system" // Германия — «Система»
  | "last_stand"; // Польша — «Глухая оборона»

export type NationalAbility = {
  id: NationalAbilityId;
  /** Короткое название способности (как у штабных способностей). */
  name: string;
  /** Развёрнутое описание для глоссария увеличенной карточки штаба. */
  description: string;
  /** Сжатая формулировка для боковой панели штаба. */
  short: string;
  /** Числовой бонус способности (+2 к защите/здоровью/атаке, +1 топливо). */
  bonus: number;
};

